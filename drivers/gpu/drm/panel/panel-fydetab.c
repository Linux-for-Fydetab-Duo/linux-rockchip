static int pnc357db1_4_enable(struct drm_panel *panel)
{
    struct pnc357db1_4 *ctx = to_pnc357db1_4(panel);
    int ret;

    ret = pnc357db1_4_init_sequence(ctx);
    if (ret < 0)
        return ret;

    if (ctx->backlight) {
        ctx->backlight->props.power = FB_BLANK_UNBLANK;
        ctx->backlight->props.state &= ~BL_CORE_FBBLANK;
        backlight_update_status(ctx->backlight);
    }

    return 0;
}

static int pnc357db1_4_disable(struct drm_panel *panel)
{
    struct pnc357db1_4 *ctx = to_pnc357db1_4(panel);

    if (ctx->backlight) {
        ctx->backlight->props.power = FB_BLANK_POWERDOWN;
        ctx->backlight->props.state |= BL_CORE_FBBLANK;
        backlight_update_status(ctx->backlight);
    }

    return 0;
}

static const struct drm_panel_funcs pnc357db1_4_drm_funcs = {
    .prepare = pnc357db1_4_prepare,
    .unprepare = pnc357db1_4_unprepare,
    .enable = pnc357db1_4_enable,
    .disable = pnc357db1_4_disable,
};

static int pnc357db1_4_probe(struct mipi_dsi_device *dsi)
{
    struct device *dev = &dsi->dev;
    struct pnc357db1_4 *ctx;
    int ret;

    ctx = devm_kzalloc(dev, sizeof(*ctx), GFP_KERNEL);
    if (!ctx)
        return -ENOMEM;

    ctx->dsi = dsi;

    ctx->reset_gpio = devm_gpiod_get(dev, "reset", GPIOD_OUT_LOW);
    if (IS_ERR(ctx->reset_gpio))
        return PTR_ERR(ctx->reset_gpio);

    ctx->vcc_avee = devm_regulator_get(dev, "vcc_avee");
    if (IS_ERR(ctx->vcc_avee))
        return PTR_ERR(ctx->vcc_avee);

    ctx->backlight = devm_of_find_backlight(dev);
    if (IS_ERR(ctx->backlight))
        return PTR_ERR(ctx->backlight);

    drm_panel_init(&ctx->panel, dev, &pnc357db1_4_drm_funcs,
                   DRM_MODE_CONNECTOR_DSI);

    drm_panel_add(&ctx->panel);

    dsi->mode_flags = MIPI_DSI_MODE_VIDEO | MIPI_DSI_MODE_VIDEO_BURST |
                      MIPI_DSI_MODE_LPM | MIPI_DSI_MODE_EOT_PACKET;
    dsi->format = MIPI_DSI_FMT_RGB888;
    dsi->lanes = 4;

    ret = mipi_dsi_attach(dsi);
    if (ret < 0) {
        drm_panel_remove(&ctx->panel);
        return ret;
    }

    return 0;
}

static int pnc357db1_4_remove(struct mipi_dsi_device *dsi)
{
    struct pnc357db1_4 *ctx = mipi_dsi_get_drvdata(dsi);

    drm_panel_remove(&ctx->panel);
    drm_panel_disable(&ctx->panel);
    drm_panel_unprepare(&ctx->panel);

    return 0;
}

